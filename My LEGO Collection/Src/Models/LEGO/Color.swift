
import Foundation



/// A color in the LEGO color palette.
///
struct LEGO_Color {
    
    
    /// The color's user displayable name e.g. "Dark Turquoise".
    ///
    var name: String
    
    
    /// The color's RGB values.
    ///
    var rgb: RGB
    
    
    /// Whether the color is transparent.
    ///
    var transparent: Bool
}
